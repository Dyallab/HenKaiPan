func TestSlugify(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  string
	}{
		{"basic", "Hello World", "hello-world"},
		{"special chars", "API Security Guide!", "api-security-guide"},
		{"multiple dashes", "foo---bar", "foo-bar"},
		{"trailing dash", "hello-", "hello"},
		{"leading dash", "-hello", "hello"},
		{"unicode", "Security à la carte", "security-la-carte"},
		{"empty", "", ""},
		{"already slug", "hello-world", "hello-world"},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			assert.Equal(t, tt.want, Slugify(tt.input))
		})
	}
}

