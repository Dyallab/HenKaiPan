func TestBuildGeneratedArticleFallback(t *testing.T) {
	ac := repository.ArticleCreate{
		Slug:          "test-slug",
		Title:         "Test Title",
		ContentMD:     "# Content",
		Tags:          []string{"tag1"},
		CWEIDs:        []string{"CWE-79"},
		RuleIDs:       []string{"rule1"},
		Scanner:       "semgrep",
		AutoGenerated: true,
	}
	article := BuildGeneratedArticleFallback(ac)
	assert.Equal(t, ac.Slug, article.Slug)
	assert.Equal(t, ac.Title, article.Title)
	assert.Equal(t, ac.ContentMD, article.ContentMD)
	assert.Equal(t, ac.Tags, article.Tags)
	assert.Equal(t, ac.CWEIDs, article.CWEIDs)
	assert.Equal(t, ac.RuleIDs, article.RuleIDs)
	assert.Equal(t, ac.AutoGenerated, article.AutoGenerated)
}

func TestNilToEmpty(t *testing.T) {
	assert.Equal(t, []string{}, nilToEmpty(nil))
	assert.Equal(t, []string{}, nilToEmpty([]string{}))
	assert.Equal(t, []string{"a"}, nilToEmpty([]string{"a"}))
}
