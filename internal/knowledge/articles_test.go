func TestBuildRemediationRequest(t *testing.T) {
	src := &repository.RemediationSource{
		RuleID:      "CVE-2024-21626",
		Title:       "runc breakout",
		Description: "runc file descriptor leak",
		Severity:    "critical",
		Scanner:     "grype",
		FilePath:    "/go.sum",
		CodeSnippet: "runc v1.1.11",
		CVEID:       "CVE-2024-21626",
		CWEID:       "CWE-403",
	}
	result := BuildRemediationRequest(src)

	assert.Equal(t, src.RuleID, result.RuleID)
	assert.Equal(t, src.Title, result.Title)
	assert.Equal(t, src.Description, result.Description)
	assert.Equal(t, src.Severity, result.Severity)
	assert.Equal(t, src.CVEID, result.CVEID)
	assert.Equal(t, src.CWEID, result.CWEID)
	assert.Equal(t, src.FilePath, result.FilePath)
	assert.Equal(t, src.CodeSnippet, result.CodeSnippet)
}

func TestBuildGeneratedArticle(t *testing.T) {
	src := &repository.RemediationSource{
		RuleID: "CVE-2024-21626",
		Title:  "runc breakout",
		Scanner: "grype",
		CWEID:  "CWE-403",
	}
	article := BuildGeneratedArticle(src, "## Remediation\nUpdate runc to v1.1.12")

	assert.Equal(t, "ai-cve-2024-21626-runc-breakout", article.Slug)
	assert.Equal(t, src.Title, article.Title)
	assert.Equal(t, "## Remediation\nUpdate runc to v1.1.12", article.ContentMD)
	assert.Equal(t, []string{"ai-generated"}, article.Tags)
	assert.Equal(t, []string{"CWE-403"}, article.CWEIDs)
	assert.Equal(t, []string{"CVE-2024-21626"}, article.RuleIDs)
	assert.Equal(t, "grype", article.Scanner)
	assert.True(t, article.AutoGenerated)
}

